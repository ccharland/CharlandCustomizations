<#
.SYNOPSIS
    Validates that all exported functions and release scripts have comment-based help.
.DESCRIPTION
    Imports the CharlandCustomizations module and checks every exported function for a
    valid .SYNOPSIS. Also validates all .ps1 scripts under the Scripts directory.
    Additionally, scans Public/*.ps1 files for dual-level help compliance: each file
    must have both a script-level help block (wrapper) and a function-level help block
    inside any defined functions.
    Functions missing help or returning the default (function name as synopsis) are
    flagged as failures.
.PARAMETER ModulePath
    Path to the module manifest. Defaults to src/CharlandCustomizations/CharlandCustomizations.psd1.
.PARAMETER ScriptPath
    Path to the Scripts directory. Defaults to Scripts.
.EXAMPLE
    ./Scripts/Test-HelpCompliance.ps1
    Validates help for all exported functions, scripts, and public files.
.EXAMPLE
    ./Scripts/Test-HelpCompliance.ps1 -ModulePath ./src/CharlandCustomizations/CharlandCustomizations.psd1
    Validates help using an explicit module path.
.OUTPUTS
    PSCustomObject

    If any functions or scripts fail validation, objects are emitted with Name, Type, and Issue properties.
.NOTES
    Generated by Kiro using Auto, reviewed by ccharland
#>
[CmdletBinding()]
[Diagnostics.CodeAnalysis.SuppressMessageAttribute('PSAvoidUsingWriteHost', '',
    Justification = 'Gate script uses Write-Host for high-visibility pass/fail status in release workflows')]
param(
    [Parameter()]
    [string]$ModulePath = (Join-Path (Split-Path $PSScriptRoot -Parent) 'src/CharlandCustomizations/CharlandCustomizations.psd1'),

    [Parameter()]
    [string]$ScriptPath = (Join-Path (Split-Path $PSScriptRoot -Parent) 'Scripts')
)

$ErrorActionPreference = 'Stop'

if (-not (Test-Path -Path $ModulePath)) {
    throw "Module manifest not found: $ModulePath"
}

if (-not (Test-Path -Path $ScriptPath)) {
    throw "Scripts directory not found: $ScriptPath"
}

# Import module so Get-Help can discover function help
Import-Module $ModulePath -Force

$manifest = Import-PowerShellDataFile -Path $ModulePath

$failures = @()

# Validate exported functions
$moduleName = (Get-Item $ModulePath).BaseName
$loadedModule = Get-Module -Name $moduleName | Select-Object -First 1
# DefaultCommandPrefix from manifest — .Prefix on the module object only reflects Import-Module -Prefix
$prefix = if ($manifest.DefaultCommandPrefix) { [string]$manifest.DefaultCommandPrefix.Trim() } else { '' }

foreach ($function in $manifest.FunctionsToExport) {
    # DefaultCommandPrefix inserts between the verb and noun: Verb-PrefixNoun
    if ($prefix) {
        $dashIndex = $function.IndexOf('-')
        if ($dashIndex -gt 0) {
            $verb = $function.Substring(0, $dashIndex)
            $noun = $function.Substring($dashIndex + 1)
            $lookupName = '{0}-{1}{2}' -f $verb, $prefix, $noun
        }
        else {
            $lookupName = $function
        }
    }
    else {
        $lookupName = $function
    }
    Write-Verbose "Checking function: $function (lookup: $lookupName)"

    # Use Get-Command to find the function after module import
    $cmd = Get-Command -Name $lookupName -ErrorAction SilentlyContinue |
    Where-Object { $_.Source -eq $moduleName }
    # Fall back to any match if module filtering misses nested module exports
    if (-not $cmd) {
        $cmd = Get-Command -Name $lookupName -ErrorAction SilentlyContinue | Select-Object -First 1
    }

    if ($cmd) {
        $help = Get-Help $cmd.Name -ErrorAction SilentlyContinue
    }
    else {
        $help = $null
    }

    if ($Null -eq $help) {
        $failures += [PSCustomObject]@{
            Name  = $function
            Type  = 'Function'
            Issue = 'No help found'
        }
    }
    elseif (
        [string]::IsNullOrWhiteSpace($help.Synopsis) -or
        $help.Synopsis -eq $function -or
        ($cmd -and $help.Synopsis -eq $cmd.Name) -or
        $help.Synopsis -match "^\s*$([regex]::Escape($lookupName))\b"
    ) {
        # PowerShell returns the function name or syntax as Synopsis when no help block exists
        $failures += [PSCustomObject]@{
            Name  = $function
            Type  = 'Function'
            Issue = 'Missing .SYNOPSIS (returns default)'
        }
    }
}

# Validate Scripts/*.ps1
$scripts = Get-ChildItem -Path $ScriptPath -Filter '*.ps1'
foreach ($script in $scripts) {
    Write-Verbose "Checking script: $($script.Name)"
    $help = Get-Help $script.FullName -ErrorAction SilentlyContinue

    # Read first non-blank line to detect auto-generated synopsis
    $firstLine = Get-Content -Path $script.FullName | Where-Object { $_.Trim() } | Select-Object -First 1

    if (-not $help -or [string]::IsNullOrWhiteSpace($help.Synopsis)) {
        $failures += [PSCustomObject]@{
            Name  = $script.Name
            Type  = 'Script'
            Issue = 'Missing .SYNOPSIS'
        }
    }
    elseif (
        $help.Synopsis -eq $script.FullName -or
        $help.Synopsis -eq $script.Name -or
        $help.Synopsis -match "^\s*$([regex]::Escape($script.Name))\b" -or
        ($firstLine -and $help.Synopsis.Trim() -eq $firstLine.Trim())
    ) {
        $failures += [PSCustomObject]@{
            Name  = $script.Name
            Type  = 'Script'
            Issue = 'Missing .SYNOPSIS (returns default)'
        }
    }
}

# Validate Public/*.ps1 files have both script-level and function-level help
$publicPath = Join-Path (Split-Path $ModulePath -Parent) 'Public'
$publicScripts = @()
if (Test-Path -Path $publicPath) {
    $publicScripts = @(Get-ChildItem -Path $publicPath -Filter '*.ps1' -Recurse |
        Where-Object { $_.Name -ne '.gitkeep' })
}

foreach ($pubScript in $publicScripts) {
    Write-Verbose "Checking dual-level help: $($pubScript.Name)"

    # Check script-level help via Get-Help on the file path
    $scriptHelp = Get-Help $pubScript.FullName -ErrorAction SilentlyContinue

    if (-not $scriptHelp -or [string]::IsNullOrWhiteSpace($scriptHelp.Synopsis) -or
        $scriptHelp.Synopsis -eq $pubScript.FullName -or
        $scriptHelp.Synopsis -eq $pubScript.Name) {
        $failures += [PSCustomObject]@{
            Name  = $pubScript.Name
            Type  = 'PublicScript'
            Issue = 'Missing script-level .SYNOPSIS (file wrapper help)'
        }
    }

    # Check function-level help by parsing for a function definition and its help block
    $content = Get-Content -Path $pubScript.FullName -Raw
    # Match function definitions (handles "function Verb-Noun" pattern)
    $functionMatches = [regex]::Matches($content, '(?m)^\s*function\s+([\w-]+)')

    if ($functionMatches.Count -eq 0) {
        Write-Verbose "  No function definition found in $($pubScript.Name), skipping function-level check."
    }
    else {
        foreach ($funcMatch in $functionMatches) {
            $funcName = $funcMatch.Groups[1].Value
            # Look for a comment-based help block (.SYNOPSIS) after the function declaration
            $funcIndex = $funcMatch.Index
            $afterFunc = $content.Substring($funcIndex)
            if ($afterFunc -notmatch '(?s)\.\s*SYNOPSIS') {
                $failures += [PSCustomObject]@{
                    Name  = "$($pubScript.Name) -> $funcName"
                    Type  = 'PublicFunction'
                    Issue = 'Missing function-level .SYNOPSIS'
                }
            }
        }
    }
}

if ($failures.Count -gt 0) {
    Write-Host "Help compliance failed. $($failures.Count) issue(s) found:" -ForegroundColor Red
    $failures | Format-Table Type, Name, Issue -AutoSize | Out-Host

    throw 'One or more functions or scripts are missing required comment-based help.'
}

$publicCount = $publicScripts.Count
Write-Host "Help compliance passed. Validated $($manifest.FunctionsToExport.Count) function(s), $($scripts.Count) script(s), and $publicCount public file(s) for dual-level help." -ForegroundColor Green
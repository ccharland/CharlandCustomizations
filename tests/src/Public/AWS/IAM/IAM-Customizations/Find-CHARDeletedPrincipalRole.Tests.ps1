Describe 'Find-CHARDeletedPrincipalRole' -Tag 'Unit' {

    Context 'Detects deleted principals in trust policies' {

        It 'Returns result when trust policy contains a deleted principal unique ID' {
            $trustDoc = @{
                Version   = '2012-10-17'
                Statement = @(
                    @{
                        Sid       = 'TrustStatement'
                        Effect    = 'Allow'
                        Principal = @{ AWS = 'AROAEXAMPLEDELETED1234' }
                        Action    = 'sts:AssumeRole'
                    }
                )
            } | ConvertTo-Json -Depth 10

            Mock Get-IAMRoleList {
                @([PSCustomObject]@{
                    RoleName                 = 'TestRole'
                    Arn                      = 'arn:aws:iam::123456789012:role/TestRole'
                    AssumeRolePolicyDocument = [System.Web.HttpUtility]::UrlEncode($trustDoc)
                })
            } -ModuleName 'IAM-Customizations'

            $result = @(Find-CHARDeletedPrincipalRole)

            $result.Count | Should -Be 1
            $result[0].RoleName | Should -Be 'TestRole'
            $result[0].RoleArn | Should -Be 'arn:aws:iam::123456789012:role/TestRole'
            $result[0].Statement | Should -Be 'TrustStatement'
            $result[0].DeletedPrincipal | Should -Be 'AROAEXAMPLEDELETED1234'
        }

        It 'Detects multiple different unique ID prefixes' {
            $trustDoc = @{
                Version   = '2012-10-17'
                Statement = @(
                    @{
                        Sid       = 'UserTrust'
                        Effect    = 'Allow'
                        Principal = @{ AWS = 'AIDAEXAMPLE123456789A' }
                        Action    = 'sts:AssumeRole'
                    },
                    @{
                        Sid       = 'RoleTrust'
                        Effect    = 'Allow'
                        Principal = @{ AWS = 'AROAEXAMPLE987654321B' }
                        Action    = 'sts:AssumeRole'
                    }
                )
            } | ConvertTo-Json -Depth 10

            Mock Get-IAMRoleList {
                @([PSCustomObject]@{
                    RoleName                 = 'MultiTrustRole'
                    Arn                      = 'arn:aws:iam::123456789012:role/MultiTrustRole'
                    AssumeRolePolicyDocument = [System.Web.HttpUtility]::UrlEncode($trustDoc)
                })
            } -ModuleName 'IAM-Customizations'

            $result = @(Find-CHARDeletedPrincipalRole)

            $result.Count | Should -Be 2
            $result[0].DeletedPrincipal | Should -Be 'AIDAEXAMPLE123456789A'
            $result[1].DeletedPrincipal | Should -Be 'AROAEXAMPLE987654321B'
        }
    }

    Context 'No deleted principals found' {

        It 'Returns nothing when trust policies have valid service principals' {
            $trustDoc = @{
                Version   = '2012-10-17'
                Statement = @(
                    @{
                        Effect    = 'Allow'
                        Principal = @{ Service = 'lambda.amazonaws.com' }
                        Action    = 'sts:AssumeRole'
                    }
                )
            } | ConvertTo-Json -Depth 10

            Mock Get-IAMRoleList {
                @([PSCustomObject]@{
                    RoleName                 = 'ServiceRole'
                    Arn                      = 'arn:aws:iam::123456789012:role/ServiceRole'
                    AssumeRolePolicyDocument = [System.Web.HttpUtility]::UrlEncode($trustDoc)
                })
            } -ModuleName 'IAM-Customizations'

            $result = @(Find-CHARDeletedPrincipalRole)

            $result.Count | Should -Be 0
        }

        It 'Returns nothing when trust policies use valid account ARNs' {
            $trustDoc = @{
                Version   = '2012-10-17'
                Statement = @(
                    @{
                        Effect    = 'Allow'
                        Principal = @{ AWS = 'arn:aws:iam::987654321098:root' }
                        Action    = 'sts:AssumeRole'
                    }
                )
            } | ConvertTo-Json -Depth 10


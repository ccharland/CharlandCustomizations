                        Resource = 'arn:aws:s3:::my-bucket/*'
                    }
                )
            } | ConvertTo-Json -Depth 10

            Mock Get-IAMPolicyList {
                @([PSCustomObject]@{
                    PolicyName       = 'S3ReadPolicy'

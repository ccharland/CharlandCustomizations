Describe 'Find-CHARDeletedPrincipalPolicy' -Tag 'Unit' {

    Context 'Detects deleted principals in policy statements' {

        It 'Returns result when policy contains a deleted principal unique ID' {
            $policyDoc = @{
                Version   = '2012-10-17'
                Statement = @(
                    @{
                        Sid       = 'AllowAccess'
                        Effect    = 'Allow'
                        Principal = @{ AWS = 'AROAEXAMPLEDELETED1234' }
                        Action    = 'sts:AssumeRole'
                        Resource  = '*'
                    }
                )
            } | ConvertTo-Json -Depth 10

            Mock Get-IAMPolicyList {
                @([PSCustomObject]@{
                    PolicyName       = 'TestPolicy'
                    Arn              = 'arn:aws:iam::123456789012:policy/TestPolicy'
                    DefaultVersionId = 'v1'
                })
            } -ModuleName 'IAM-Customizations'

            Mock Get-IAMPolicyVersion {
                [PSCustomObject]@{
                    Document = [System.Web.HttpUtility]::UrlEncode($policyDoc)
                }
            } -ModuleName 'IAM-Customizations'

            $result = @(Find-CHARDeletedPrincipalPolicy -Scope Local)

            $result.Count | Should -Be 1
            $result[0].PolicyName | Should -Be 'TestPolicy'
            $result[0].PolicyArn | Should -Be 'arn:aws:iam::123456789012:policy/TestPolicy'
            $result[0].VersionId | Should -Be 'v1'
            $result[0].Statement | Should -Be 'AllowAccess'
            $result[0].DeletedPrincipal | Should -Be 'AROAEXAMPLEDELETED1234'
        }

        It 'Returns multiple results for multiple deleted principals in different statements' {
            $policyDoc = @{
                Version   = '2012-10-17'
                Statement = @(
                    @{
                        Sid       = 'Statement1'
                        Effect    = 'Allow'
                        Principal = @{ AWS = 'AIDAEXAMPLE123456789A' }
                        Action    = 's3:GetObject'
                        Resource  = '*'
                    },
                    @{
                        Sid       = 'Statement2'
                        Effect    = 'Allow'
                        Principal = @{ AWS = 'AROAEXAMPLE987654321B' }
                        Action    = 's3:PutObject'
                        Resource  = '*'
                    }
                )
            } | ConvertTo-Json -Depth 10

            Mock Get-IAMPolicyList {
                @([PSCustomObject]@{
                    PolicyName       = 'MultiPrincipalPolicy'
                    Arn              = 'arn:aws:iam::123456789012:policy/MultiPrincipalPolicy'
                    DefaultVersionId = 'v2'
                })
            } -ModuleName 'IAM-Customizations'

            Mock Get-IAMPolicyVersion {
                [PSCustomObject]@{
                    Document = [System.Web.HttpUtility]::UrlEncode($policyDoc)
                }
            } -ModuleName 'IAM-Customizations'

            $result = @(Find-CHARDeletedPrincipalPolicy -Scope Local)

            $result.Count | Should -Be 2
            $result[0].Statement | Should -Be 'Statement1'
            $result[0].DeletedPrincipal | Should -Be 'AIDAEXAMPLE123456789A'
            $result[1].Statement | Should -Be 'Statement2'
            $result[1].DeletedPrincipal | Should -Be 'AROAEXAMPLE987654321B'
        }
    }


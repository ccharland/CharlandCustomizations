Describe 'GitCustomizations module loader' -Tag 'Unit' {

    Context 'Module file is syntactically valid' {

        It 'GitCustomizations.psm1 parses without errors' {
            $psm1Path = "$PSScriptRoot/../../../../../src/CharlandCustomizations/Public/Git/GitCustomizations.psm1"
            $errors = $null
            [System.Management.Automation.Language.Parser]::ParseFile($psm1Path, [ref]$null, [ref]$errors) | Out-Null
            $errors | Should -BeNullOrEmpty
        }
    }

    Context 'Module exports expected functions' {

        BeforeAll {
            Get-Module GitCustomizations -All | Remove-Module -Force -ErrorAction SilentlyContinue

            # Stub external dependencies that the dot-sourced functions require
            if (-not (Get-Command chmod -ErrorAction SilentlyContinue)) {
                function global:chmod { }
            }
            if (-not (Get-Command gpg -ErrorAction SilentlyContinue)) {
                function global:gpg { }
            }
            if (-not (Get-Command Get-AuthenticodeSignature -ErrorAction SilentlyContinue)) {
                function global:Get-AuthenticodeSignature { param($FilePath) }
            }

            Import-Module "$PSScriptRoot/../../../../../src/CharlandCustomizations/Public/Git/GitCustomizations.psm1" -Force
        }

        AfterAll {
            Get-Module GitCustomizations -All | Remove-Module -Force -ErrorAction SilentlyContinue
            Remove-Item 'function:global:chmod' -ErrorAction SilentlyContinue
            Remove-Item 'function:global:gpg' -ErrorAction SilentlyContinue
            Remove-Item 'function:global:Get-AuthenticodeSignature' -ErrorAction SilentlyContinue
        }

        It 'Exports Install-CHARGitHook' {
            Get-Command -Module GitCustomizations -Name Install-CHARGitHook -ErrorAction SilentlyContinue |
                Should -Not -BeNullOrEmpty
        }

        It 'Exports Test-CHARCommitSignature' {
            Get-Command -Module GitCustomizations -Name Test-CHARCommitSignature -ErrorAction SilentlyContinue |
                Should -Not -BeNullOrEmpty
        }
    }
}

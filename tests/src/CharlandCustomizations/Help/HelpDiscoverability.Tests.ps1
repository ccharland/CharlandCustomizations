# Read manifest at script scope so Pester discovery can resolve the function list
$manifest = Import-PowerShellDataFile "$PSScriptRoot/../../../../src/CharlandCustomizations/CharlandCustomizations.psd1"
$exportedFunctions = $manifest.FunctionsToExport

BeforeAll {
    # Import the manifest data to get FunctionsToExport list
    $script:manifest = Import-PowerShellDataFile "$PSScriptRoot/../../../../src/CharlandCustomizations/CharlandCustomizations.psd1"
    $script:exportedFunctions = $script:manifest.FunctionsToExport

    # Import the module itself so Get-Help can resolve function help
    Get-Module CharlandCustomizations -All | Remove-Module -Force -ErrorAction SilentlyContinue
    Import-Module "$PSScriptRoot/../../../../src/CharlandCustomizations/CharlandCustomizations.psd1" -Force
}

Describe 'Help Discoverability' -Tag 'Help' {
    # Feature: pester-test-coverage, Property 9: Help discoverability for all exported functions
    # Validates: Requirements 7.1, 7.2, 7.3, 7.4

    Context '<_>' -ForEach $exportedFunctions {
        BeforeAll {
            # Function names in FunctionsToExport already include the CC prefix
            $script:commandName = $_
        }

        It 'has a non-empty Synopsis that is not auto-generated' {
            $help = Get-Help -Name $script:commandName -ErrorAction SilentlyContinue
            $help.Synopsis | Should -Not -BeNullOrEmpty
            # Auto-generated synopsis just repeats the function name
            $help.Synopsis.Trim() | Should -Not -Be $script:commandName
        }

        It 'has a non-empty Description' {
            $help = Get-Help -Name $script:commandName -ErrorAction SilentlyContinue
            $help.Description | Should -Not -BeNullOrEmpty
        }

        It 'has at least one Example' {
            $help = Get-Help -Name $script:commandName -Full -ErrorAction SilentlyContinue
            $help.Examples.Example.Count | Should -BeGreaterOrEqual 1
        }
    }
}

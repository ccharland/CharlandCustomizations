<#
.SYNOPSIS
    Validates that every exported function and private helper has a corresponding
    test file with at least one It block.
.DESCRIPTION
    Uses the module manifest FunctionsToExport as the source of truth for public
    functions. Private helpers are discovered by directory scan. Module-level files
    (manifest and root module) are checked separately.

    Expected test layout under tests/src/:
      - Public functions:  <FunctionName>.Tests.ps1 (anywhere under tests/src/)
      - Private helpers:   Private/<FileName>.Tests.ps1
      - Module manifest:   CharlandCustomizations.psd1.Tests.ps1
      - Root module:       CharlandCustomizations.psm1.Tests.ps1
.NOTES
    Generated by Kiro using Auto, reviewed by ccharland
#>
BeforeAll {
    $script:RepoRoot = Resolve-Path (Join-Path $PSScriptRoot '..\..')
    $script:SourceRoot = Join-Path $script:RepoRoot 'src/CharlandCustomizations'
    $script:TestRoot = Join-Path $script:RepoRoot 'tests/src'
    $script:ManifestPath = Join-Path $script:SourceRoot 'CharlandCustomizations.psd1'
}

Describe 'SRC test layout gate' -Tag 'Unit' {

    It 'Every exported function has a .Tests.ps1 with at least one It block' {
        $manifest = Import-PowerShellDataFile -Path $script:ManifestPath
        $exportedFunctions = @($manifest.FunctionsToExport)

        # Build a lookup of all test file basenames under tests/src/
        $testFiles = Get-ChildItem -Path $script:TestRoot -Recurse -File -Filter '*.Tests.ps1'
        $testBaseNames = $testFiles | ForEach-Object {
            [System.IO.Path]::GetFileNameWithoutExtension($_.Name) -replace '\.Tests$', ''
        }

        $missing = @()
        $missingIt = @()

        foreach ($functionName in $exportedFunctions) {
            # Find a test file named <FunctionName>.Tests.ps1 anywhere under tests/src/
            $matchingTest = $testFiles | Where-Object {
                $_.Name -eq "$functionName.Tests.ps1"
            } | Select-Object -First 1

            if (-not $matchingTest) {
                $missing += $functionName
                continue
            }

            # Verify the test file contains at least one It block
            $rawTest = Get-Content -Path $matchingTest.FullName -Raw
            if ($rawTest -notmatch '(?m)^\s*It\s+[\"'']') {
                $missingIt += $functionName
            }
        }

        $missing.Count | Should -Be 0 -Because (
            "Exported functions missing test files: {0}" -f ($missing -join ', ')
        )
        $missingIt.Count | Should -Be 0 -Because (
            "Test files missing an It block: {0}" -f ($missingIt -join ', ')
        )
    }

    It 'Every private helper .ps1 has a .Tests.ps1 with at least one It block' {
        $privateDir = Join-Path $script:SourceRoot 'Private'
        $privateTestDir = Join-Path $script:TestRoot 'Private'

        $privateFiles = Get-ChildItem -Path $privateDir -Filter '*.ps1' |
            Where-Object { $_.Name -ne '.gitkeep' }

        $missing = @()
        $missingIt = @()

        foreach ($file in $privateFiles) {
            $testFileName = [System.IO.Path]::GetFileNameWithoutExtension($file.Name) + '.Tests.ps1'
            $expectedPath = Join-Path $privateTestDir $testFileName

            if (-not (Test-Path $expectedPath)) {
                $missing += $file.Name
                continue
            }

            $rawTest = Get-Content -Path $expectedPath -Raw
            if ($rawTest -notmatch '(?m)^\s*It\s+[\"'']') {
                $missingIt += $file.Name
            }
        }

        $missing.Count | Should -Be 0 -Because (
            "Private helpers missing test files: {0}" -f ($missing -join ', ')
        )
        $missingIt.Count | Should -Be 0 -Because (
            "Private test files missing an It block: {0}" -f ($missingIt -join ', ')
        )
    }

    It 'Module manifest has a test file' {
        $testPath = Join-Path $script:TestRoot 'CharlandCustomizations.psd1.Tests.ps1'
        Test-Path $testPath | Should -BeTrue -Because 'CharlandCustomizations.psd1.Tests.ps1 must exist'

        $rawTest = Get-Content -Path $testPath -Raw
        $rawTest | Should -Match '(?m)^\s*It\s+[\"'']' -Because 'Manifest test must contain at least one It block'
    }

    It 'Root module has a test file' {
        $testPath = Join-Path $script:TestRoot 'CharlandCustomizations.psm1.Tests.ps1'
        Test-Path $testPath | Should -BeTrue -Because 'CharlandCustomizations.psm1.Tests.ps1 must exist'

        $rawTest = Get-Content -Path $testPath -Raw
        $rawTest | Should -Match '(?m)^\s*It\s+[\"'']' -Because 'Root module test must contain at least one It block'
    }
}

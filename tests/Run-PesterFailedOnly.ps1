<#
.NOTES
    Generated by GitHub Copilot, reviewed by ccharland
.SYNOPSIS
    Runs Pester tests and prints only failed tests.
.DESCRIPTION
    Loads the shared Pester configuration and suppresses normal test output,
    then prints only failures. Returns exit code 1 when any test fails.
.EXAMPLE
    pwsh -NoProfile -File ./tests/Run-PesterFailedOnly.ps1
.EXAMPLE
    pwsh -NoProfile -File ./tests/Run-PesterFailedOnly.ps1 -Compact
#>
[CmdletBinding()]
param(
    [Parameter()]
    [switch]$Compact
)

$configPath = Join-Path $PSScriptRoot 'pester.config.ps1'
$resultPath = Join-Path $PSScriptRoot 'coverage/testResults.xml'

# Run Pester in a child process with output redirected to keep terminal noise out.
$command = @"
`$config = & '$configPath'
`$config.Run.Exit = `$false
`$config.Output.Verbosity = 'None'
Invoke-Pester -Configuration `$config | Out-Null
"@

$encodedCommand = [Convert]::ToBase64String([Text.Encoding]::Unicode.GetBytes($command))
$psi = [System.Diagnostics.ProcessStartInfo]::new()
$psi.FileName = 'pwsh'
$null = $psi.ArgumentList.Add('-NoProfile')
$null = $psi.ArgumentList.Add('-EncodedCommand')
$null = $psi.ArgumentList.Add($encodedCommand)
$psi.UseShellExecute = $false
$psi.RedirectStandardOutput = $true
$psi.RedirectStandardError = $true
$psi.CreateNoWindow = $true

$process = [System.Diagnostics.Process]::Start($psi)
$null = $process.StandardOutput.ReadToEnd()
$stdErr = $process.StandardError.ReadToEnd()
$process.WaitForExit()

if (-not (Test-Path -Path $resultPath)) {
    if ($stdErr) {
        Write-Error "Pester run did not produce test results. Stderr: $stdErr"
    }
    else {
        Write-Error "Pester run did not produce test results at '$resultPath'."
    }
    exit 2
}

[xml]$testResults = Get-Content -Path $resultPath -Raw
$failedTests = @($testResults.SelectNodes('//test-case[@result="Failed" or @success="False"]'))

if ($failedTests.Count -eq 0) {
    Write-Host 'No failed tests.'
    exit 0
}

$rows = foreach ($testCase in $failedTests) {
    [PSCustomObject]@{
        Name    = [string]$testCase.name
        Message = [string]$testCase.failure.message
    }
}

if ($Compact) {
    $rows | Select-Object Name | Format-Table -AutoSize
}
else {
    $rows | Format-List
}

exit 1

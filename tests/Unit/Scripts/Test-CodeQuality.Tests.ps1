BeforeAll {
    # The SUT is a script with CmdletBinding and param block, so we dot-source it
    # wrapped in a function for testability
    $script:SUTPath = "$PSScriptRoot/../../../Scripts/Test-CodeQuality.ps1"
}

Describe 'Test-CodeQuality' -Tag 'Unit' {

    BeforeEach {
        # Mock module availability check so script doesn't try to install PSScriptAnalyzer
        Mock Get-Module { [PSCustomObject]@{ Name = 'PSScriptAnalyzer'; Version = '1.21.0' } } -ParameterFilter { $ListAvailable -eq $true -and $Name -eq 'PSScriptAnalyzer' }
        Mock Import-Module {}
        Mock Write-Host {}
    }

    Context 'Invokes PSScriptAnalyzer on correct paths' {

        It 'Calls Invoke-ScriptAnalyzer with the default Path targeting src/CharlandCustomizations' {
            # Arrange
            Mock Invoke-ScriptAnalyzer { return $null }

            # Act - invoke the script with defaults
            & $script:SUTPath

            # Assert (Req 1.2, 1.3, 1.4, 10.1)
            Should -Invoke Invoke-ScriptAnalyzer -Times 1 -Exactly -ParameterFilter {
                $Path -like '*src/CharlandCustomizations' -and
                $Recurse -eq $true -and
                $Severity -contains 'Error' -and
                $Severity -contains 'Warning'
            }
        }

        It 'Calls Invoke-ScriptAnalyzer with a custom Path when -Path is specified' {
            # Arrange
            Mock Invoke-ScriptAnalyzer { return $null }
            $customPath = 'TestDrive:\CustomModule'

            # Act
            & $script:SUTPath -Path $customPath

            # Assert
            Should -Invoke Invoke-ScriptAnalyzer -Times 1 -Exactly -ParameterFilter {
                $Path -eq $customPath -and
                $Recurse -eq $true
            }
        }

        It 'Passes -Severity parameter to Invoke-ScriptAnalyzer' {
            # Arrange
            Mock Invoke-ScriptAnalyzer { return $null }

            # Act
            & $script:SUTPath -Severity 'Error'

            # Assert
            Should -Invoke Invoke-ScriptAnalyzer -Times 1 -Exactly -ParameterFilter {
                $Severity -contains 'Error' -and
                $Severity.Count -eq 1
            }
        }

        It 'Passes -ExcludeRule parameter to Invoke-ScriptAnalyzer when specified' {
            # Arrange
            Mock Invoke-ScriptAnalyzer { return $null }

            # Act
            & $script:SUTPath -ExcludeRule 'PSAvoidUsingWriteHost'

            # Assert
            Should -Invoke Invoke-ScriptAnalyzer -Times 1 -Exactly -ParameterFilter {
                $ExcludeRule -contains 'PSAvoidUsingWriteHost'
            }
        }

        It 'Passes -Fix parameter to Invoke-ScriptAnalyzer when -Fix switch is used' {
            # Arrange
            Mock Invoke-ScriptAnalyzer { return $null }

            # Act
            & $script:SUTPath -Fix

            # Assert
            Should -Invoke Invoke-ScriptAnalyzer -Times 1 -Exactly -ParameterFilter {
                $Fix -eq $true
            }
        }
    }

    Context 'Result handling' {

        It 'Writes success message when no issues are found' {
            # Arrange
            Mock Invoke-ScriptAnalyzer { return $null }

            # Act
            & $script:SUTPath

            # Assert - Write-Host is called with the success message
            Should -Invoke Write-Host -ParameterFilter {
                $Object -like '*No issues found*'
            }
        }
    }
}

        function global:Invoke-TestPathPolicyHook {
            param(
                [Parameter(Mandatory)]
                [string]$BranchName,

                [Parameter(Mandatory)]
                [string]$StagedPath,

                [Parameter(Mandatory)]
                [string]$HookRepo,

                [Parameter(Mandatory)]
                [string]$GitPath,

                [Parameter(Mandatory)]
                [string]$HookPath
            )

            try {
                Push-Location $HookRepo
                & $GitPath checkout -q -b $BranchName

                $fullPath = Join-Path $HookRepo $StagedPath
                New-Item -Path (Split-Path $fullPath -Parent) -ItemType Directory -Force | Out-Null
                Set-Content -Path $fullPath -Value 'test content'
                & $GitPath add $StagedPath

                $output = & sh $HookPath 2>&1
                $exitCode = $LASTEXITCODE
            }
            finally {
                Pop-Location
            }

            [PSCustomObject]@{
                ExitCode = $exitCode
                Output   = ($output | Out-String)
            }
        }


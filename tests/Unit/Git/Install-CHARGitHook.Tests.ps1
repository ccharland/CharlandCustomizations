        Mock git {
            param()
            # Determine which git subcommand was called based on args
            if ($args -contains '--git-dir') {
                return "TestDrive:\repo\.git"
            }
            if ($args -contains '--show-toplevel') {
                return "TestDrive:\repo"
            }
            # For git config calls, return empty
            return $null
        }

        # Suppress LASTEXITCODE from git mocks
        $global:LASTEXITCODE = 0

        # Mock Write-Host to suppress output
        Mock Write-Host {}

        # Mock chmod for cross-platform compatibility
        Mock chmod {}

        # Mock gpg to prevent smartcard/certificate dialogs during signing verification
        Mock gpg { return 'mocked' }
    }

    Context 'Source hook copied to destination' {

        It 'Copies source hook file to .git/hooks/pre-commit with matching content' {
            # Arrange - create source hook
            $sourceContent = '#!/bin/sh' + "`n" + 'echo "running pre-commit hook"'
            Set-Content -Path "TestDrive:\repo\.githooks\pre-commit" -Value $sourceContent -NoNewline

            # Mock git to return proper paths
            Mock git {
                if ($args -contains '--git-dir') {
                    return (Join-Path $TestDrive 'repo\.git')
                }
                if ($args -contains '--show-toplevel') {
                    return (Join-Path $TestDrive 'repo')
                }
                return $null
            }
            $global:LASTEXITCODE = 0

            # Act
            Install-CHARGitHook

            # Assert - destination file exists with matching content
            $destPath = Join-Path $TestDrive 'repo\.git\hooks\pre-commit'
            $destPath | Should -Exist
            $destContent = Get-Content -Path $destPath -Raw
            $srcContent = Get-Content -Path (Join-Path $TestDrive 'repo\.githooks\pre-commit') -Raw
            $destContent | Should -Be $srcContent
        }
    }

    Context 'Existing hook without -Force prompts user' {

        It 'Calls Read-Host to prompt user when hook already exists and -Force not specified' {
            # Arrange - create source and existing destination hook
            $sourceContent = '#!/bin/sh' + "`n" + 'echo "new hook"'

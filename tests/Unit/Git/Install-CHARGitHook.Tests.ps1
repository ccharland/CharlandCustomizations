                if ($args -contains '--git-dir') {
                    return (Join-Path $TestDrive 'repo\.git')
                }
                if ($args -contains '--show-toplevel') {
                    return (Join-Path $TestDrive 'repo')
                }
                return $null
            }
            $global:LASTEXITCODE = 0

            # Mock Read-Host to return 'y' (accept overwrite)
            Mock Read-Host { return 'y' }

            # Act
            Install-CHARGitHook

            # Assert - Read-Host was called
            Should -Invoke Read-Host -Times 1 -Exactly
        }
    }

    Context 'Force overwrites without prompt' {


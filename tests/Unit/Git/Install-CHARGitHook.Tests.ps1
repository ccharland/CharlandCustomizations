                & $script:PathPolicyGit init -q
                & $script:PathPolicyGit config user.email 'test@example.com'
                & $script:PathPolicyGit config user.name 'Pester Test'
            }
            finally {
                Pop-Location
            }
        }

        AfterEach {
            if (Get-Location -Stack) {
                Pop-Location -ErrorAction SilentlyContinue

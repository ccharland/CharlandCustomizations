            $script:callCount = 0
            $sb = { $script:callCount++ }

            Invoke-ScriptMultiAccountRegion -ProfileName 'profile1', 'profile2' `
                -Region 'us-east-1', 'us-west-2', 'eu-west-1' `
                -ScriptBlock $sb

            $script:callCount | Should -Be 6
        }

        It 'Executes ScriptBlock N×M times for 3 profiles and 2 regions' {
            $script:callCount = 0
            $sb = { $script:callCount++ }

            Invoke-ScriptMultiAccountRegion -ProfileName 'dev', 'staging', 'prod' `
                -Region 'us-east-1', 'eu-west-1' `
                -ScriptBlock $sb

            $script:callCount | Should -Be 6
        }

        It 'Executes ScriptBlock once for 1 profile and 1 region' {
            $script:callCount = 0
            $sb = { $script:callCount++ }

            Invoke-ScriptMultiAccountRegion -ProfileName 'single' `
                -Region 'us-east-1' `
                -ScriptBlock $sb

            $script:callCount | Should -Be 1
        }
    }

    Context 'IncludeAccountId enrichment (Req 2.9)' {

        It 'Adds AccountId property matching Get-STSCallerIdentity account' {

            $sb = { [PSCustomObject]@{ Name = 'TestResource' } }

            $results = Invoke-ScriptMultiAccountRegion -ProfileName 'myprofile' `
                -Region 'us-east-1' `
                -ScriptBlock $sb `
                -IncludeAccountId


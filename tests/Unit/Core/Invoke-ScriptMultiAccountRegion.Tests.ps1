            $capturedWarnings | Should -Not -BeNullOrEmpty
            $warningText = $capturedWarnings | Out-String
            $warningText | Should -BeLike '*failprofile*'
            $warningText | Should -BeLike '*us-west-2*'
            $warningText | Should -BeLike '*Access Denied*'
        }
    }


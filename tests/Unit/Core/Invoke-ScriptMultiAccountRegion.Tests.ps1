            # First profile returns one account, second returns another
            Mock Get-STSCallerIdentity {
                if ($ProfileName -eq 'dev') {
                    [PSCustomObject]@{ Account = '111111111111'; Arn = 'arn:aws:iam::111111111111:user/test' }
                } else {
                    [PSCustomObject]@{ Account = '222222222222'; Arn = 'arn:aws:iam::222222222222:user/test' }
                }
            }

            $sb = { [PSCustomObject]@{ Name = 'Resource' } }

            $results = Invoke-ScriptMultiAccountRegion -ProfileName 'dev', 'prod' `
                -Region 'us-east-1' `
                -ScriptBlock $sb `
                -IncludeAccountId

            $results | Should -HaveCount 2
            $results[0].AccountId | Should -Be '111111111111'
            $results[1].AccountId | Should -Be '222222222222'
        }
    }


Describe 'Install-CCProfilesFromSource' -Tag 'Unit' {

    BeforeAll {
        # Set up a fake $PROFILE object with NoteProperty members
        $script:MockProfile = [PSCustomObject]@{
            AllUsersAllHosts       = '/tmp/fake/AllUsersAllHosts.ps1'
            AllUsersCurrentHost    = '/tmp/fake/AllUsersCurrentHost.ps1'
            CurrentUserAllHosts    = '/tmp/fake/CurrentUserAllHosts.ps1'
            CurrentUserCurrentHost = '/tmp/fake/CurrentUserCurrentHost.ps1'
        }
    }

    Context 'Copies matching profile files to corresponding $PROFILE paths' {

        It 'Copies each matching profile file to the correct $PROFILE destination' {
            # Arrange
            $sourcePath = 'TestDrive:\Profiles'
            New-Item -Path $sourcePath -ItemType Directory -Force

            # Create source files matching profile type names
            New-Item -Path (Join-Path $sourcePath 'CurrentUserAllHosts') -ItemType File -Force
            New-Item -Path (Join-Path $sourcePath 'CurrentUserCurrentHost') -ItemType File -Force

            # Mock $PROFILE to return our controlled object
            Mock Get-Member {
                @(
                    [PSCustomObject]@{ Name = 'CurrentUserAllHosts'; MemberType = 'NoteProperty' }
                    [PSCustomObject]@{ Name = 'CurrentUserCurrentHost'; MemberType = 'NoteProperty' }
                )
            } -ParameterFilter { $InputObject -eq $PROFILE -and $MemberType -eq 'NoteProperty' }

            Mock Test-Path { return $true } -ParameterFilter { $Path -like '*CurrentUserAllHosts' -or $Path -like '*CurrentUserCurrentHost' }
            Mock Copy-Item {}

            # Act
            Install-CCProfilesFromSource -Path $sourcePath

            # Assert (Req 2.1) — Copy-Item called for each matching profile file
            Should -Invoke Copy-Item -Times 2 -Exactly
        }
    }

    Context 'WhatIf support' {

        It 'Does not write files when -WhatIf is specified' {
            # Arrange
            $sourcePath = 'TestDrive:\ProfilesWhatIf'
            New-Item -Path $sourcePath -ItemType Directory -Force
            New-Item -Path (Join-Path $sourcePath 'CurrentUserAllHosts') -ItemType File -Force

            Mock Get-Member {
                @(
                    [PSCustomObject]@{ Name = 'CurrentUserAllHosts'; MemberType = 'NoteProperty' }
                )
            } -ParameterFilter { $InputObject -eq $PROFILE -and $MemberType -eq 'NoteProperty' }

            Mock Test-Path { return $true } -ParameterFilter { $Path -like '*CurrentUserAllHosts' }
            Mock Copy-Item {}

            # Act
            Install-CCProfilesFromSource -Path $sourcePath -WhatIf

            # Assert (Req 2.2) — Copy-Item should NOT be called with -WhatIf
            Should -Invoke Copy-Item -Times 0 -Exactly
        }
    }
}

Describe 'Find-CHARCWAlarmInvalidAction' -Tag 'Unit' {

    BeforeEach {
        Mock Get-SNSTopic {
            @(
                [PSCustomObject]@{ TopicArn = 'arn:aws:sns:us-east-1:123456789012:valid-topic' }
            )
        } -ModuleName 'CloudWatch-Alarms'
    }

    It 'Returns nothing when all alarm actions are valid SNS topics' {
        Mock Get-CWAlarm {
            @([PSCustomObject]@{
                    AlarmName               = 'healthy-alarm'
                    AlarmArn                = 'arn:aws:cloudwatch:us-east-1:123456789012:alarm:healthy-alarm'
                    AlarmActions            = @('arn:aws:sns:us-east-1:123456789012:valid-topic')
                    OKActions               = @()
                    InsufficientDataActions = @()
                })
        } -ModuleName 'CloudWatch-Alarms'

        $result = Find-CHARCWAlarmInvalidAction

        $result | Should -BeNullOrEmpty
    }

    It 'Returns an alarm whose SNS action ARN no longer exists' {
        Mock Get-CWAlarm {
            @([PSCustomObject]@{
                    AlarmName               = 'stale-alarm'
                    AlarmArn                = 'arn:aws:cloudwatch:us-east-1:123456789012:alarm:stale-alarm'
                    AlarmActions            = @('arn:aws:sns:us-east-1:123456789012:deleted-topic')
                    OKActions               = @()
                    InsufficientDataActions = @()
                })
        } -ModuleName 'CloudWatch-Alarms'

        $result = Find-CHARCWAlarmInvalidAction

        $result | Should -Not -BeNullOrEmpty
        $result.AlarmName | Should -Be 'stale-alarm'
        $result.InvalidActions | Should -Contain 'arn:aws:sns:us-east-1:123456789012:deleted-topic'
    }

    It 'Returns an alarm whose action ARN has an invalid format' {
        Mock Get-CWAlarm {
            @([PSCustomObject]@{
                    AlarmName               = 'bad-arn-alarm'
                    AlarmArn                = 'arn:aws:cloudwatch:us-east-1:123456789012:alarm:bad-arn-alarm'
                    AlarmActions            = @('not-a-valid-arn')
                    OKActions               = @()
                    InsufficientDataActions = @()
                })
        } -ModuleName 'CloudWatch-Alarms'

        $result = Find-CHARCWAlarmInvalidAction

        $result | Should -Not -BeNullOrEmpty
        $result.AlarmName | Should -Be 'bad-arn-alarm'
        $result.InvalidActions | Should -Contain 'not-a-valid-arn'
    }

    It 'Detects invalid actions across OKActions and InsufficientDataActions too' {
        Mock Get-CWAlarm {
            @([PSCustomObject]@{
                    AlarmName               = 'multi-state-alarm'
                    AlarmArn                = 'arn:aws:cloudwatch:us-east-1:123456789012:alarm:multi-state-alarm'
                    AlarmActions            = @()
                    OKActions               = @('arn:aws:sns:us-east-1:123456789012:missing-ok-topic')
                    InsufficientDataActions = @('arn:aws:sns:us-east-1:123456789012:missing-insuff-topic')
                })
        } -ModuleName 'CloudWatch-Alarms'

        $result = Find-CHARCWAlarmInvalidAction

        $result | Should -Not -BeNullOrEmpty
        $result.InvalidActions.Count | Should -Be 2
    }

    It 'Returns nothing when there are no alarms' {
        Mock Get-CWAlarm { @() } -ModuleName 'CloudWatch-Alarms'

        $result = Find-CHARCWAlarmInvalidAction

        $result | Should -BeNullOrEmpty
    }

